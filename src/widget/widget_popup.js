export function WidgetPopUp1({ props, children }) {
  const { close, title, desc, btnTitle, submit, error, loading } = props;
  return (
    <div className="zpA">
      <div className="zpAa">
        <div className="zpAb">
          <div className="zpAbA">{title}</div>
          <div className="zpAbB" onClick={close} />
        </div>
        <div className="zpAc">{desc}</div>
        <div className="zpAd">{children}</div>
        <div className="zpAe">
          {error !== null ? <div className="zpAeA">{error}</div> : <div />}
          <div className="zpAeB">
            <div className="zpAeBa" onClick={close}>
              CANCEL
            </div>
            <div className={loading ? "zpAeBb_" : "zpAeBb"} onClick={submit}>
              {btnTitle || "SAVE"}
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}
export function WidgetPopUp1Body({ children }) {
  return <div className="zpAf">{children}</div>;
}
export function WidgetPopUp1In1({ children, title }) {
  return (
    <div className="zpAg">
      <div className="zpAgA">{title}</div>
      {children}
    </div>
  );
}

